#include<stdio.h>
int main(void)
{
    //variable declarations
    int i_SSP;

    //code
    printf("\n\n");

    printf("Printing digits 1 to 10: \n\n");

    i_SSP=1;
    do
    {
         printf("\t%d\n",i_SSP);
        i_SSP++;
    } while (i_SSP<=10);
    printf("\n\n");

    return 0;
    
}
