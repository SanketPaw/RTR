#include<stdio.h>
int main(void)
{
    //variables declarations
    int i_SSP;

    //code
    printf("\n\n");

    printf("Printing digits 10 to 1 : \n\n");

    for(i_SSP=10;i_SSP>=1;i_SSP--)
    {
        printf("\t%d\n",i_SSP);
    }

    printf("\n\n");
}
